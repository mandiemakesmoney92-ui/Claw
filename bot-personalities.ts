// Relationship graph for bot-to-bot interactions
export function getBotRelationship(from: string, to: string): "likes" | "argues" | "admires" | "ignores" | "neutral" {
  const bot = BOT_PERSONALITIES.find(b => b.username === from);
  if (!bot) return "neutral";
  if (bot.relationships.likes?.includes(to)) return "likes";
  if (bot.relationships.argues?.includes(to)) return "argues";
  if (bot.relationships.admires?.includes(to)) return "admires";
  if (bot.relationships.ignores?.includes(to)) return "ignores";
  return "neutral";
}

export function getActiveBots(count = 5): BotPersonality[] {
  const shuffled = [...BOT_PERSONALITIES].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
